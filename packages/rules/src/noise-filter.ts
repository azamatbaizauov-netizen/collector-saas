// Дешёвый префильтр перед вызовом LLM: не тратим Haiku на очевидный «шум» —
// голые подтверждения/приветствия/эмодзи без цифр и без смысловых слов. Такие
// сообщения всё равно дают intent=OTHER и никакой даты/суммы, разбор их бесполезен.
//
// Консервативно: сомневаемся — считаем НЕ шумом (пусть решает LLM). Наличие цифры
// сразу снимает подозрение (возможен срок/сумма/остаток). Это НЕ трогает счётчик
// касаний (WhatsAppTouch пишется до AI-секции) — режем только AI-вызов.

const ACK_WORDS = new Set([
  'ок', 'окей', 'ok', 'okay', 'k', 'хорошо', 'хор', 'спасибо', 'спс', 'благодарю',
  'пожалуйста', 'да', 'ага', 'угу', 'ясно', 'понял', 'поняла', 'принял', 'принято',
  'договорились', 'здравствуйте', 'привет', 'добрый', 'день', 'утро', 'вечер',
  'салам', 'салем', 'рахмет', 'пока', 'до', 'свидания',
]);

const MAX_ACK_WORDS = 3;

export function isNoiseMessage(text: string): boolean {
  const trimmed = text.trim();
  if (trimmed === '') return true;
  // Есть цифра → возможен срок/сумма/остаток → не шум.
  if (/\d/.test(trimmed)) return false;

  // Оставляем только буквы (эмодзи/пунктуация → пробел).
  const letters = trimmed.toLowerCase().replace(/[^\p{L}]+/gu, ' ').trim();
  if (letters === '') return true; // только эмодзи/знаки препинания

  const words = letters.split(/\s+/);
  if (words.length > MAX_ACK_WORDS) return false; // фраза — пусть разбирает LLM
  return words.every((w) => ACK_WORDS.has(w));
}
