import type Anthropic from '@anthropic-ai/sdk';
import { z } from 'zod';

// Дневная кластеризация ленты рабочей WhatsApp-группы «чеки» (ADR 0008). На вход —
// весь транскрипт группы за сутки по порядку (текст + строки разбора чеков vision'ом).
// Задача Sonnet: понять по СМЫСЛУ, сколько РАЗНЫХ людей вернуло долг сегодня и на
// какую сумму. Это не жёсткое правило «по числу чеков»: один менеджер может кинуть
// несколько чеков + скрин курса по ОДНОМУ платежу одного клиента, а текст «Иван
// вернул 500» — это отдельное событие возврата. Сумму НЕ конвертируем между валют:
// возвращаем по каждому платежу его валюту, суммирование по валютам — уже в коде
// (правила, а не магия — CLAUDE.md принцип 5).
const MODEL = 'claude-sonnet-4-6';

export interface DayPayer {
  // Имя/идентификатор человека, вернувшего долг, как его можно понять из ленты
  // (плательщик на чеке, имя из текста). Если не ясно — короткое описание.
  name: string;
  // Сумма возврата ЧИСЛОМ в валюте платежа (не конвертируем).
  amountMajor: number;
  // ISO-код валюты: KZT | USD | RUB | CNY.
  currency: string;
  // Менеджер, отчитавшийся о возврате — берётся из префикса «Менеджер X:» строки
  // ленты, где появился этот платёж. «—», если отправитель не сопоставлен.
  manager: string;
}

export interface DaySummary {
  payers: DayPayer[];
}

const outputSchema = z.object({
  payers: z
    .array(
      z.object({
        name: z.string(),
        amount: z.number(),
        currency: z.string(),
        manager: z.string().nullish(),
      }),
    )
    .default([]),
});

const PROMPT = `Ты анализируешь за сутки ленту рабочей WhatsApp-группы оптовика, куда менеджеры и клиенты скидывают подтверждения ВОЗВРАТА долга: чеки оплаты (обычно Kaspi, тенге), скрины курса обмена, и текстом пишут «такой-то вернул такую-то сумму».

Твоя задача — понять по СМЫСЛУ, сколько РАЗНЫХ людей сегодня вернуло долг и на какую сумму каждый. Это НЕ «сколько чеков»: рассуждай, кто за чем стоит.

Как рассуждать:
- Несколько чеков подряд от одного менеджера по одному клиенту + скрин курса к ним — это ОДИН возврат одного человека (курс объясняет пересчёт, а не отдельный платёж).
- Текст «Иван вернул 500» без чека — это отдельное событие возврата (человек Иван).
- Скрин курса обмена сам по себе — НЕ платёж, не считай его как возврат.
- Фото товара, болтовня, приветствия — игнорируй.
- Если один и тот же платёж виден и чеком, и текстом — не удваивай.
- Сомневаешься, платёж это или нет — не включай.

Каждая строка ленты начинается с префикса «Менеджер X:» — это менеджер, который запостил сообщение (отчитался о возврате своего клиента). Если отправитель не сопоставлен, префикс «Менеджер —:».

Для каждого установленного человека верни: name (кто вернул — имя плательщика с чека или из текста; если не понять — краткое описание), amount (сумма ЧИСЛОМ в валюте платежа, НЕ конвертируй), currency (ISO: KZT/USD/RUB/CNY), manager (имя менеджера из префикса той строки ленты, где виден этот платёж; если строк несколько по одному платежу — берётся тот же менеджер; «—», если не понять).

Валюту НЕ конвертируем: чек Kaspi — это тенге (KZT). Если у платежа две валюты (сумма в тенге + пометка курса к доллару) — бери валюту фактического платежа (тенге).

Ответь ТОЛЬКО JSON, без пояснений. Если возвратов за день нет — верни пустой массив:
{"payers":[{"name":"Иван","amount":500,"currency":"USD","manager":"Адильбек"},{"name":"Альфа ТОО","amount":250000,"currency":"KZT","manager":"Айнур"}]}`;

export async function summarizeDayPayments(
  client: Anthropic,
  transcript: string,
): Promise<DaySummary> {
  const message = await client.messages.create({
    model: MODEL,
    max_tokens: 1024,
    temperature: 0,
    messages: [
      {
        role: 'user',
        content: `${PROMPT}\n\n=== ЛЕНТА ГРУППЫ ЗА СЕГОДНЯ ===\n${transcript}`,
      },
    ],
  });

  const raw = message.content[0];
  if (!raw || raw.type !== 'text') throw new Error('Unexpected AI response type');

  const jsonMatch = raw.text.match(/\{[\s\S]*\}/);
  if (!jsonMatch) throw new Error('No JSON in AI response');

  const parsed = outputSchema.parse(JSON.parse(jsonMatch[0]));

  return {
    payers: parsed.payers.map((p) => ({
      name: p.name,
      amountMajor: p.amount,
      currency: p.currency,
      manager: p.manager ?? '—',
    })),
  };
}
